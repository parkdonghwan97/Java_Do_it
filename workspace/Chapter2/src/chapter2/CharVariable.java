package chapter2;

public class CharVariable {

	public static void main(String[] args) {
		
		char ch ='A';
		System.out.println(ch);
		
		System.out.println((int)ch);   // A  문자열을 아스키코드 값으로 출력 
		
		int ch2 = 66;
		System.out.println((char)ch2); // B
		System.out.println(ch2); // 66
	}

}
