package assignment;

public class OperationEx2 {

	public static void main(String[] args) {

		int num =10;
		
		// 증감 연산자 
		System.out.println(num);    //10
		System.out.println(num++);  //10
		System.out.println(num);	//11
		
		System.out.println(num);	//11
		System.out.println(++num);  //12
		System.out.println(num);	//12
		
		// -- 연산자도 마찬가지
		
		System.out.println(num);    //12
		System.out.println(num--);  //12
		System.out.println(num);	//11
		
		System.out.println(num);	//11
		System.out.println(--num);  //10
		System.out.println(num);	//10
		
		
		
				
	}

}
